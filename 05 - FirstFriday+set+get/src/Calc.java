
public class Calc {
	public int suma(int a, int b) {
		System.out.println("Sum.Su sveikais skaiciais");
		return a + b;
	}
	public float suma(float a, float b) {
		System.out.println("Sum.Su kableliais");
		return a + b;
	}
	public int daugyba(int a, int b) {
		System.out.println("Dau.Su sveikais skaiciais");
		return a * b;
	}
	public float daugyba(float a, float b) {
		System.out.println("Dau.Su kableliais");
		return a * b;
	}
	public int dalyba(int a, int b) {
		System.out.println("Dal.Su sveikais skaiciais");
		return a / b;
	}
	public float dalyba(float a, float b) {
		System.out.println("Dal.Su kableliais");
		return a / b;
	}
	public int atimtis(int a, int b) {
		System.out.println("Min.Su sveikais skaiciais");
		return a - b;
	}
	public float atimtis(float a, float b) {
		System.out.println("Min.Su kableliais");
		return a - b;
	}
	
}
